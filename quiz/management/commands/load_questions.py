    def load_from_api(self, direction, api_category):
        """Загружает вопросы из OpenTriviaDB API"""
        url = f"https://opentdb.com/api.php?amount=10&category={api_category}&type=multiple"
        
        try:
            response = requests.get(url, timeout=10)
            data = response.json()
            
            if data.get('response_code') != 0:
                self.stdout.write(self.style.ERROR(f"  Ошибка API: {data.get('response_code')}"))
                return False
            
            questions_data = data.get('results', [])
            
            for q in questions_data:
                # Декодируем HTML сущности
                text = html.unescape(q['question'])
                correct = html.unescape(q['correct_answer'])
                incorrect = [html.unescape(ans) for ans in q['incorrect_answers']]
                
                # Перемешиваем варианты
                options = incorrect + [correct]
                random.shuffle(options)
                correct_index = options.index(correct) + 1
                
                Question.objects.create(
                    direction=direction,
                    text=text,
                    option1=options[0],
                    option2=options[1],
                    option3=options[2],
                    option4=options[3],
                    correct_option=correct_index
                )
            
            self.stdout.write(self.style.SUCCESS(f"  ✓ Загружено {len(questions_data)} вопросов из API для {direction.name}"))
            return True
            
        except Exception as e:
            self.stdout.write(self.style.ERROR(f"  Ошибка подключения к API: {e}"))
            return False
    
